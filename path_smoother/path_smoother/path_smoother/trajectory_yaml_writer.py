"""
Trajectory YAML Writer
=======================
Writes the generated trajectory to a YAML file in the assignment format:

    trajectory = [(x0, y0, t0), (x1, y1, t1), ..., (xn, yn, tn)]

This is a standalone utility module — it has no ROS2 dependency.
The node calls write_trajectory_yaml() if the parameter
save_trajectory_yaml is set to true in params.yaml.

To DISABLE writing, either:
  Option A — set the parameter in params.yaml:
      save_trajectory_yaml: false

  Option B — comment out the single call in trajectory_generator_node.py:
      # ── TRAJECTORY YAML SAVE (comment this block to disable) ──────
      # if self.save_trajectory_yaml:
      #     write_trajectory_yaml(trajectory, self.trajectory_yaml_path)
      # ── END TRAJECTORY YAML SAVE ──────────────────────────────────
"""

import os
from typing import List, Tuple
from path_smoother.trajectory_generator import TrajectoryPoint


def trajectory_to_yaml(
    trajectory: List[TrajectoryPoint],
) -> str:
    """
    Serialise trajectory into a human-readable YAML string.

    The file stores two representations side by side:
      1. A YAML list-of-mappings (machine-readable, loadable by any YAML parser)
      2. A commented Python-style tuple list (matches assignment notation exactly)

    Args:
        trajectory: List of TrajectoryPoint objects from generate_trajectory().

    Returns:
        YAML string ready to write to file.
    """
    lines = []

    # ── Header ────────────────────────────────────────────────────────────────
    lines.append("# ============================================================")
    lines.append("# Trajectory Points — generated by trajectory_generator_node")
    lines.append("# ============================================================")
    lines.append("#")
    lines.append("# Assignment format (Python tuples) — for reference:")
    lines.append("#")
    lines.append("#   trajectory = [")
    for i, tp in enumerate(trajectory):
        comma = "," if i < len(trajectory) - 1 else ""
        lines.append(f"#       ({tp.x:.4f}, {tp.y:.4f}, {tp.t:.4f}){comma}")
    lines.append("#   ]")
    lines.append("#")
    lines.append(f"# Total points : {len(trajectory)}")
    if trajectory:
        lines.append(f"# Duration     : {trajectory[-1].t:.4f} s")
    lines.append("# ============================================================")
    lines.append("")

    # ── Machine-readable YAML list ────────────────────────────────────────────
    lines.append("trajectory:")
    for tp in trajectory:
        lines.append(f"  - x:     {tp.x:.6f}")
        lines.append(f"    y:     {tp.y:.6f}")
        lines.append(f"    t:     {tp.t:.6f}")
        # lines.append(f"    vx:    {tp.vx:.6f}")
        # lines.append(f"    vy:    {tp.vy:.6f}")
        # lines.append(f"    speed: {tp.speed:.6f}")

    return "\n".join(lines) + "\n"


def write_trajectory_yaml(
    trajectory: List[TrajectoryPoint],
    output_path: str,
) -> None:
    """
    Write trajectory to a YAML file.

    Creates the output directory if it does not exist.
    Overwrites any existing file at output_path.

    Args:
        trajectory:  List of TrajectoryPoint objects.
        output_path: Destination file path (e.g. 'config/trajectory_points.yaml').

    Raises:
        ValueError: If trajectory is empty.
        OSError:    If the file cannot be written.
    """
    if not trajectory:
        raise ValueError("Cannot write empty trajectory to YAML.")

    os.makedirs(os.path.dirname(os.path.abspath(output_path)), exist_ok=True)

    yaml_str = trajectory_to_yaml(trajectory)
    with open(output_path, "w") as f:
        f.write(yaml_str)


def load_trajectory_yaml(
    input_path: str,
) -> List[Tuple[float, float, float]]:
    """
    Load trajectory from a YAML file back into (x, y, t) tuples.

    Useful for replaying or inspecting a saved trajectory.

    Args:
        input_path: Path to a trajectory YAML file written by this module.

    Returns:
        List of (x, y, t) tuples — the assignment format.

    Raises:
        FileNotFoundError: If input_path does not exist.
        KeyError:          If the YAML is missing expected fields.
    """
    try:
        import yaml
    except ImportError:
        raise ImportError("PyYAML is required: pip install pyyaml")

    with open(input_path, "r") as f:
        data = yaml.safe_load(f)

    return [(pt["x"], pt["y"], pt["t"]) for pt in data["trajectory"]]
