#!/usr/bin/env python3
"""
Waypoint Editor
===============
Accepts waypoints as command-line x,y pairs in the assignment format:
    waypoints = [(x0, y0), (x1, y1), ..., (xn, yn)]

Writes them to config/waypoints.yaml, then automatically triggers:
    ros2 launch path_smoother path_smoother.launch.py

Usage
-----
    python3 waypoint_editor.py x0,y0 x1,y1 ... xn,yn [OPTIONS]

Examples
--------
    # Basic — update waypoints and launch
    python3 waypoint_editor.py 0,0 1,0.5 2,1.2 3,1.0 4,0

    # With sim time disabled (real robot)
    python3 waypoint_editor.py 0,0 1,0.5 2,1.2 --no-sim-time

    # Custom waypoints file output path
    python3 waypoint_editor.py 0,0 1,0.5 2,1.2 --output /abs/path/waypoints.yaml

    # Dry run — write file but do NOT launch
    python3 waypoint_editor.py 0,0 1,0.5 2,1.2 --dry-run

Requirements
------------
    - ROS2 must be sourced before running this script
    - Package must be built: colcon build --packages-select path_smoother
"""

import sys
import os
import argparse
import subprocess
from typing import List, Tuple


# ══════════════════════════════════════════════════════════════════════════════
# Validation
# ══════════════════════════════════════════════════════════════════════════════

def validate_waypoints(waypoints: List[Tuple[float, float]]) -> None:
    """
    Validate waypoints before writing to file.

    Args:
        waypoints: List of (x, y) tuples.

    Raises:
        ValueError: If fewer than 2 waypoints or any value is invalid.
    """
    if len(waypoints) < 2:
        raise ValueError(
            f"At least 2 waypoints required, got {len(waypoints)}."
        )
    for i, wp in enumerate(waypoints):
        x, y = wp
        if x != x or y != y:   # NaN check
            raise ValueError(f"Waypoint {i} contains NaN: ({x}, {y})")


# ══════════════════════════════════════════════════════════════════════════════
# CLI parsing
# ══════════════════════════════════════════════════════════════════════════════

def parse_cli_waypoints(tokens: List[str]) -> List[Tuple[float, float]]:
    """
    Parse waypoints from command-line x,y tokens.

    Args:
        tokens: List of 'x,y' strings from sys.argv.

    Returns:
        List of (x, y) float tuples.

    Raises:
        ValueError: If any token is malformed or non-numeric.
    """
    waypoints = []
    for i, token in enumerate(tokens):
        parts = token.strip().split(",")
        if len(parts) != 2:
            raise ValueError(
                f"Argument {i+1} '{token}' must be in x,y format  "
                f"(e.g. 1.0,0.5). Got {len(parts)} value(s)."
            )
        try:
            x, y = float(parts[0]), float(parts[1])
        except ValueError:
            raise ValueError(
                f"Argument {i+1} '{token}' contains non-numeric values."
            )
        waypoints.append((x, y))
    return waypoints


# ══════════════════════════════════════════════════════════════════════════════
# YAML writer
# ══════════════════════════════════════════════════════════════════════════════

def write_waypoints_yaml(
    waypoints: List[Tuple[float, float]],
    output_path: str,
) -> None:
    """
    Write waypoints to a ROS2-compatible YAML file.

    ROS2 parameter server does not support list-of-lists natively, so
    waypoints are flattened from [(x,y),...] to [x0,y0,x1,y1,...].

    Args:
        waypoints:   List of (x, y) tuples.
        output_path: Destination file path.
    """
    # Build readable flat pairs — one (x, y) pair per line
    pair_strings = [f"{float(x)}, {float(y)}" for x, y in waypoints]
    indented     = ",\n                ".join(pair_strings)

    yaml_content = f"""\
# ============================================================
# Waypoints Configuration
# ============================================================
# Generated by waypoint_editor.py — do not edit manually.
# To update, run:
#   python3 waypoint_editor.py x0,y0 x1,y1 ... xn,yn
#
# Original assignment format:
# waypoints = {[(float(x), float(y)) for x, y in waypoints]}
#
# Total waypoints: {len(waypoints)}
# ============================================================

path_smoother_node:
  ros__parameters:
    waypoints: [{indented}]
"""

    os.makedirs(os.path.dirname(os.path.abspath(output_path)), exist_ok=True)
    with open(output_path, "w") as f:
        f.write(yaml_content)


# ══════════════════════════════════════════════════════════════════════════════
# ROS2 launch trigger
# ══════════════════════════════════════════════════════════════════════════════

def trigger_ros2_launch(use_sim_time: bool, waypoints_file: str) -> None:
    """
    Rebuild the package to pick up the new waypoints.yaml, then launch.

    Steps performed automatically:
        1. colcon build --packages-select path_smoother --symlink-install
        2. source install/setup.bash  (via shell -c)
        3. ros2 launch path_smoother path_smoother.launch.py

    Args:
        use_sim_time:   Whether to pass use_sim_time:=true to the launch.
        waypoints_file: Absolute path to the waypoints yaml file.

    Raises:
        SystemExit: If colcon build fails.
    """
    # Resolve workspace root — assumed to be 3 levels up from this script:
    # <ws>/src/path_smoother/waypoint_editor.py  →  <ws>
    script_dir = os.path.dirname(os.path.abspath(__file__))
    ws_root = os.path.abspath(os.path.join(script_dir, "..", ".."))

    sim_arg   = "true" if use_sim_time else "false"
    wp_arg    = f"waypoints_file:={waypoints_file}"

    # Single shell command: build → source → launch (chained with &&)
    shell_cmd = (
        f"cd {ws_root} && "
        f"colcon build --packages-select path_smoother --symlink-install && "
        f"source install/setup.bash && "
        f"ros2 launch path_smoother path_smoother.launch.py "
        f"use_sim_time:={sim_arg} {wp_arg}"
    )

    print("\n" + "─" * 60)
    print("  Step 1/3 — Building package...")
    print("  Step 2/3 — Sourcing workspace...")
    print("  Step 3/3 — Launching ROS2 nodes...")
    print("─" * 60 + "\n")

    try:
        # executable=True + shell=True lets us use 'source' (bash built-in)
        result = subprocess.run(
            shell_cmd,
            shell=True,
            executable="/bin/bash",
        )
        if result.returncode != 0:
            print(f"\n  ERROR: Launch sequence failed (exit code {result.returncode}).")
            print("  Check the output above for details.")
            sys.exit(result.returncode)

    except KeyboardInterrupt:
        print("\n  Launch interrupted by user.")
        sys.exit(0)


# ══════════════════════════════════════════════════════════════════════════════
# Summary printer
# ══════════════════════════════════════════════════════════════════════════════

def print_summary(
    waypoints: List[Tuple[float, float]],
    output_path: str,
    dry_run: bool,
) -> None:
    """Print a formatted summary of the operation."""
    flat = []
    for x, y in waypoints:
        flat += [float(x), float(y)]

    print("\n" + "=" * 60)
    print("  Waypoints written successfully")
    print("=" * 60)
    print(f"\n  File    : {os.path.abspath(output_path)}")
    print(f"  Count   : {len(waypoints)} waypoints\n")

    print("  Assignment format (input received):")
    print("  waypoints = [")
    for i, (x, y) in enumerate(waypoints):
        comma = "," if i < len(waypoints) - 1 else ""
        print(f"      ({x}, {y}){comma}")
    print("  ]\n")

    print("  ROS2 flat array (written to YAML):")
    print(f"  {flat}\n")

    if dry_run:
        print("  DRY RUN — skipping launch. To launch manually:")
        print("  1. colcon build --packages-select path_smoother --symlink-install")
        print("  2. source install/setup.bash")
        print("  3. ros2 launch path_smoother path_smoother.launch.py use_sim_time:=true")

    print("=" * 60 + "\n")


# ══════════════════════════════════════════════════════════════════════════════
# Entry point
# ══════════════════════════════════════════════════════════════════════════════

def main():
    script_dir      = os.path.dirname(os.path.abspath(__file__))
    default_output  = os.path.join(script_dir, "config", "waypoints.yaml")

    parser = argparse.ArgumentParser(
        prog="waypoint_editor.py",
        description=(
            "Write waypoints to config/waypoints.yaml and launch the ROS2 system.\n"
            "Accepts the assignment format: waypoints = [(x0,y0), (x1,y1), ...]"
        ),
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  python3 waypoint_editor.py 0,0 1,0.5 2,1.2 3,1.0 4,0
  python3 waypoint_editor.py 0,0 1,0.5 2,1.2 --no-sim-time
  python3 waypoint_editor.py 0,0 1,0.5 2,1.2 --dry-run
  python3 waypoint_editor.py 0,0 1,0.5 2,1.2 --output /tmp/my_waypoints.yaml
        """,
    )

    parser.add_argument(
        "waypoints",
        nargs="+",
        metavar="x,y",
        help="Waypoints as x,y pairs. Example: 0,0  1,0.5  2,1.2  3,1.0  4,0",
    )
    parser.add_argument(
        "--no-sim-time",
        action="store_true",
        default=False,
        help="Launch with use_sim_time:=false (use for real robot, default: false)",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        default=False,
        help="Write waypoints.yaml but do NOT trigger ros2 launch",
    )
    parser.add_argument(
        "--output", "-o",
        default=default_output,
        metavar="PATH",
        help=f"Output YAML file path (default: config/waypoints.yaml)",
    )

    args = parser.parse_args()

    try:
        # ── Parse and validate ────────────────────────────────────────────────
        waypoints = parse_cli_waypoints(args.waypoints)
        validate_waypoints(waypoints)

        # ── Write YAML ────────────────────────────────────────────────────────
        write_waypoints_yaml(waypoints, args.output)
        print_summary(waypoints, args.output, dry_run=args.dry_run)

        # ── Launch (unless --dry-run) ─────────────────────────────────────────
        if not args.dry_run:
            use_sim_time = not args.no_sim_time
            trigger_ros2_launch(
                use_sim_time=use_sim_time,
                waypoints_file=os.path.abspath(args.output),
            )

    except ValueError as e:
        print(f"\n  ERROR: {e}\n")
        parser.print_usage()
        sys.exit(1)


if __name__ == "__main__":
    main()
